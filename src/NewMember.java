    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       String id=jLabel3.getText();
       String name=jTextField1.getText();
        String fathername=jTextField2.getText();
        String email=jTextField3.getText();
        String mobileno=jTextField4.getText();
        String gender=(String)jComboBox1.getSelectedItem();
        String idcard=jTextField5.getText();
        String gymtime=(String)jComboBox2.getSelectedItem();
        String age=jTextField6.getText();
        String amount=jTextField7.getText();
        
        try
        {
         Connection con=ConnectionProvider.getCon();
         PreparedStatement ps=con.prepareStatement("insert into member values (?,?,?,?,?,?,?,?,?,?)");
       
         ps.setString(1, id);
         ps.setString(2, name);
         ps.setString(3, fathername);
         ps.setString(4, email);
         ps.setString(5, mobileno);
         ps.setString(6, gender);
         ps.setString(7, idcard);
         ps.setString(8, gymtime);
         ps.setString(9, age);
         ps.setString(10, amount);
        
        ps.executeUpdate();
        JOptionPane.showMessageDialog(null,"Saved Successfully");
        setVisible(false);
        new NewMember().setVisible(true);
        }
        catch(Exception e)
        {
          JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

